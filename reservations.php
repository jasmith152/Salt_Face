trip to the next available date. Cancellations due to non weather related events by
you; must be a verbal or email cancellation 72 hours in advance prior to your trip
date to receive a full refund. Or you may reschedule your trip to avoid loosing your
deposit. (Lots of time and money go into trip preparations). It takes up to 30 days
to process a refund and have it returned to you.</p>
