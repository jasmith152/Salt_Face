    <p>Reservations should be made in advance. Saturday and Sunday  are not a problem. Deposit required for all new SaltFace Charters clients.  Anglers requesting overnight excursions are billed and booked accordingly.</p>
    <p>As Captain and operator of SaltFace Charters, I cannot  always predict weather pattern changes like summer storms or cold fronts  entering or exiting Florida. Therefore, refunding fees or re-booking are at the  discretion of the Captain. Sea-sickness is also non-refundable.</p>
    <p><strong>Things to bring:</strong><br />
     &bull; Sunscreen – Florida sun can be brutal<br />
     &bull; Appropriate Clothing – hat is a must, sunglasses polarized  if you have them, in the cool months it is best to bring layers of clothes.<br />
      &bull; Food and Beverages – bring your own drinks and snacks in a personal or standard size cooler <strong>(No Oversized Coolers)</strong>.<br />
&bull; (Per customer request), personalized drinks and snack foods can be purchased, on the boat, on ice and ready to go when you arrive. <br />
&bull; You will need to bring your own coolers to keep in your car for  the fish you catch when we return.</p>
    <p><strong>Rules and Regulations</strong><br /></p>
    <p>Intentional damages to gear will not be tolerated and will  be charged for accordingly. Have fun, respect the elements, and if you intend  to release your fish, do so with skill and speed.</p>
